// Java program to demostrate the while loop. 
  
class whileLoopDemo { 
    public static void main(String args[]) 
    { 
        //expression initialization
        int i = 1; 
  
        //testing the expression 
        while (i < 6) { 
            System.out.println("This will print 5 times!!!"); 
  
            //adding the expression to repeat 
            i++; 
        } 
    } 
}